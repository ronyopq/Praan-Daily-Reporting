export const holidays = sqliteTable(
  "holidays",
  {
    id: text("id").primaryKey(),
    holidayDate: text("holiday_date").notNull(),
    holidayType: text("holiday_type").notNull().default("govt_holiday"),
    label: text("label").notNull(),
    countryCode: text("country_code").notNull().default("BD"),
    isRecurring: integer("is_recurring", { mode: "boolean" }).notNull().default(false),
    isActive: integer("is_active", { mode: "boolean" }).notNull().default(true),
    isOverrideAllowed: integer("is_override_allowed", { mode: "boolean" }).notNull().default(true),
    ...auditColumns(),
    ...softDeleteColumns(),
  },
  (table) => ({
    dateIdx: uniqueIndex("holidays_date_idx").on(table.holidayDate),
    activeIdx: index("holidays_active_idx").on(table.isActive),
  }),
);
