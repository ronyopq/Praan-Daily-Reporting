import { index, integer, sqliteTable, text, uniqueIndex } from "drizzle-orm/sqlite-core";

import { departmentsOrProjects, users } from "./access";
import { auditColumns, softDeleteColumns } from "./_shared";

export const taskTemplates = sqliteTable(
  "task_templates",
  {
    id: text("id").primaryKey(),
    userId: text("user_id").references(() => users.id, { onDelete: "cascade" }),
    title: text("title").notNull(),
    description: text("description"),
    category: text("category"),
    expectedOutput: text("expected_output"),
    linkedProjectId: text("linked_project_id").references(() => departmentsOrProjects.id),
    usageCount: integer("usage_count").notNull().default(0),
    lastUsedAt: text("last_used_at"),
    isShared: integer("is_shared", { mode: "boolean" }).notNull().default(false),
    ...auditColumns(),
    ...softDeleteColumns(),
  },
  (table) => ({
    userIdx: index("task_templates_user_idx").on(table.userId),
  }),
);

export const workPlans = sqliteTable(
  "work_plans",
  {
    id: text("id").primaryKey(),
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),
    month: text("month").notNull(),
    status: text("status").notNull().default("draft"),
    autosavedAt: text("autosaved_at"),
    submittedAt: text("submitted_at"),
    approvedAt: text("approved_at"),
    approvedBy: text("approved_by").references(() => users.id),
    reviewerComment: text("reviewer_comment"),
    ...auditColumns(),
    ...softDeleteColumns(),
  },
  (table) => ({
    userMonthIdx: uniqueIndex("work_plans_user_month_idx").on(table.userId, table.month),
    statusIdx: index("work_plans_status_idx").on(table.status),
  }),
);

export const workPlanItems = sqliteTable(
  "work_plan_items",
  {
    id: text("id").primaryKey(),
    workPlanId: text("work_plan_id")
      .notNull()
      .references(() => workPlans.id, { onDelete: "cascade" }),
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),
    month: text("month").notNull(),
    planDate: text("plan_date").notNull(),
    sortOrder: integer("sort_order").notNull().default(0),
    title: text("title").notNull(),
    description: text("description"),
    expectedOutput: text("expected_output"),
    category: text("category"),
    priority: text("priority").notNull().default("medium"),
    linkedProjectId: text("linked_project_id").references(() => departmentsOrProjects.id),
    holidayType: text("holiday_type").notNull().default("none"),
    holidayLabel: text("holiday_label"),
    notes: text("notes"),
    status: text("status").notNull().default("planned"),
    isAutoGenerated: integer("is_auto_generated", { mode: "boolean" })
      .notNull()
      .default(false),
    followUpRequired: integer("follow_up_required", { mode: "boolean" })
      .notNull()
      .default(false),
    ...auditColumns(),
    ...softDeleteColumns(),
  },
  (table) => ({
    userDateIdx: index("work_plan_items_user_date_idx").on(table.userId, table.planDate),
    monthIdx: index("work_plan_items_month_idx").on(table.month),
    statusIdx: index("work_plan_items_status_idx").on(table.status),
  }),
);

